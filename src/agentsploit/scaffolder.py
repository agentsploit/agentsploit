# ----------------------------------------------------------- public API


class ScaffoldError(Exception):
    """Raised when a scaffold target conflicts with existing content."""


def scaffold_engagement(
    target_dir: Path,
    *,
    authorized_by: str = "Operator <ops@example.com>",
    engagement_id: str | None = None,
    valid_days: int = 30,
    force: bool = False,
) -> list[Path]:
    """Create a new engagement directory at `target_dir`.

    Returns the list of created file paths (in deterministic order).
    Raises ScaffoldError if the directory already exists and `force` is False.
    """
    target_dir = target_dir.resolve()

    if target_dir.exists() and not force:
        if any(target_dir.iterdir()):
            raise ScaffoldError(
                f"{target_dir} already exists and is not empty. Use --force to overwrite."
            )

    target_dir.mkdir(parents=True, exist_ok=True)

    now = datetime.now(UTC)
    context: dict[str, str] = {
        "engagement_id": engagement_id or f"eng-{now.strftime('%Y%m%d')}-{uuid4().hex[:6]}",
        "authorized_by": authorized_by,
        "today": now.strftime("%Y-%m-%d"),
        "today_iso": now.isoformat(),
        "valid_until_iso": (now + timedelta(days=valid_days)).isoformat(),
    }

    written: list[Path] = []
    for filename, template in _FILES.items():
        path = target_dir / filename
        if path.exists() and not force:
            raise ScaffoldError(f"{path} already exists. Use --force to overwrite.")
        path.write_text(template.format(**context))
        written.append(path)

    return written
